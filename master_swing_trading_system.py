        ema50  = float(nifty_close.ewm(span=50).mean().iloc[-1])
        ema200 = float(nifty_close.ewm(span=200).mean().iloc[-1])
        price  = float(nifty_close.iloc[-1])

        trend_score = sum([
            price  > ema20,
            ema20  > ema50,
            ema50  > ema200,
            price  > ema200,
        ])  # 0–4

        # ── ADX ───────────────────────────────────────────────────────────
        nifty_full = nifty[["High", "Low", "Close"]].dropna()
        if len(nifty_full) < 20:
            log.warning("Insufficient NIFTY OHLC data for ADX. Using fallback.")
