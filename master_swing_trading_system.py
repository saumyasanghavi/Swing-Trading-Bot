        adx_ind    = ta.trend.ADXIndicator(
            nifty_full["High"], nifty_full["Low"], nifty_full["Close"], window=14
        )
        adx_series = adx_ind.adx().dropna()
        adx_val    = float(adx_series.iloc[-1]) if not adx_series.empty else 0.0

