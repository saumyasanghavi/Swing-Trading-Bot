def analyze(symbol):

    try:
        df = yf.download(symbol, period="6mo", interval="1d", progress=False, auto_adjust=True)

        if df.empty or len(df)<60:
            return None

        df["ema20"]=ta.trend.ema_indicator(df.Close,20)
        df["ema50"]=ta.trend.ema_indicator(df.Close,50)
        df["rsi"]=ta.momentum.rsi(df.Close,14)
        df["atr"]=ta.volatility.average_true_range(df.High,df.Low,df.Close,14)

        last=df.iloc[-1]

        strategies=[]
        score=0

        # TREND PULLBACK
        if last.ema20>last.ema50 and last.Close<last.ema20*1.03:
            strategies.append("Pullback Trend")
            score+=2

        # BREAKOUT
        high20=df.High.rolling(20).max().iloc[-2]
        if last.Close>high20:
            strategies.append("Breakout Momentum")
            score+=3

        # VOLATILITY EXPANSION
        if last.atr>df.atr.mean():
            strategies.append("Volatility Expansion")
            score+=1

        # MEAN REVERSION
        if last.rsi<30:
            strategies.append("Mean Reversion")
            score+=2

        if score<2:
            return None

        entry=round(last.Close,2)
        sl=round(entry-last.atr,2)
        target=round(entry+2*last.atr,2)

        return (symbol,entry,sl,target,score,", ".join(strategies))

    except:
        return None


