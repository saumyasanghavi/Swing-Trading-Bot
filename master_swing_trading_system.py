def market_sentiment():

    import yfinance as yf

    # ---------- NIFTY TREND ----------
    nifty = yf.download("^NSEI", period="3mo", progress=False)

    if nifty.empty:
        return "NEUTRAL", 0

    ema20 = nifty["Close"].ewm(span=20).mean().iloc[-1]
    ema50 = nifty["Close"].ewm(span=50).mean().iloc[-1]

    trend = 1 if float(ema20) > float(ema50) else -1


    # ---------- VIX ----------
    vix = yf.download("^INDIAVIX", period="5d", progress=False)

    if vix.empty:
        vol = 0
    else:
        v = float(vix["Close"].iloc[-1])

        if v < 15:
            vol = 1
        elif v > 20:
            vol = -1
        else:
            vol = 0


    # ---------- NEWS SENTIMENT ----------
    try:
        news = float(news_sentiment())
    except:
        news = 0


    # ---------- FINAL SCORE ----------
    score = trend + vol + news


    # ---------- INTERPRETATION ----------
    if score > 1:
        mood = "BULLISH"
    elif score < -1:
        mood = "BEARISH"
    else:
        mood = "NEUTRAL"

    return mood, round(score,2)



