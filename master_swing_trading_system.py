def build_trade_setup(
    symbol: str,
    daily,
    signal: dict,
    regime: str,
    sent_score: float,
    sent_label: str,
) -> dict:

    last  = daily.iloc[-1]
    entry = round(float(last["Close"]), 2)
    atr   = float(last["atr"])

    sl = round(entry - atr * 1.3, 2)
    t1 = round(entry + atr * 2.0, 2)
    t2 = round(entry + atr * 3.5, 2)

    risk = entry - sl
    rr   = round((t1 - entry) / risk, 2) if risk > 0 else 0

    qty, capital = position_size(entry, sl)

    return {
        "symbol": symbol,
        "entry": entry,
        "sl": sl,
        "target1": t1,
        "target2": t2,
        "rr": rr,
        "qty": qty,
        "capital_deployed": capital,
        "score": signal["score"],
        "strategies": ", ".join(signal["strategies"]),
        "direction": signal["direction"],
        "regime": regime,
        "sentiment_score": sent_score,
        "sentiment_label": sent_label,
        "timestamp": datetime.now().isoformat(),
    }


