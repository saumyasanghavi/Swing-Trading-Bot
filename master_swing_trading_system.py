def analyze_symbol(symbol: str, regime_data: dict) -> dict | None:
    """
    Full analysis pipeline for a single symbol.
    """

    regime = regime_data.get("regime", "RANGING")

    # ─────────────────────────────────────────────────────────────────────
    # 1. Fundamental Filter
    # ─────────────────────────────────────────────────────────────────────
    passes, _ = fundamental_filter(symbol)
    if not passes:
        return None

    # ─────────────────────────────────────────────────────────────────────
    # 2. Fetch Multi-Timeframe Data
    # ─────────────────────────────────────────────────────────────────────
    mtf = fetch_mtf_data(symbol)
    if not mtf:
        return None

    # ─────────────────────────────────────────────────────────────────────
    # 3. Indicator Computation
    # ─────────────────────────────────────────────────────────────────────
    try:
        daily  = compute_indicators(mtf["daily"].copy())
        weekly = compute_indicators(mtf["weekly"].copy())
        hourly = mtf.get("hourly")
    except Exception as e:
        log.warning(f"[{symbol}] Indicator computation failed: {e}")
        return None

    # ─────────────────────────────────────────────────────────────────────
    # 4. Weekly Trend Bias
    # ─────────────────────────────────────────────────────────────────────
    w_bias = weekly_trend_bias(weekly)

    if regime == "STRONG_BEAR" and w_bias == 1:
        return None

    # ─────────────────────────────────────────────────────────────────────
    # 5. Signal Scoring
    # ─────────────────────────────────────────────────────────────────────
    signal = score_signals(daily, w_bias, regime)

    if not signal["strategies"]:
        return None

    if signal["score"] < CONFIG["MIN_SCORE"]:
        return None

    # ─────────────────────────────────────────────────────────────────────
    # 6. Hourly Confirmation
    # ─────────────────────────────────────────────────────────────────────
    if not hourly_entry_confirmation(hourly, signal["direction"]):
        return None

    # ─────────────────────────────────────────────────────────────────────
    # 7. Risk : Reward Check
    # ─────────────────────────────────────────────────────────────────────
    last  = daily.iloc[-1]
    entry = round(float(last["Close"]), 2)
    atr   = float(last["atr"])

    sl     = round(entry - atr * 1.3, 2)
    target = round(entry + atr * 2.6, 2)

    risk   = entry - sl
    reward = target - entry
    rr     = round(reward / risk, 2) if risk > 0 else 0

    if rr < CONFIG["MIN_RR"]:
        return None

    # ─────────────────────────────────────────────────────────────────────
    # 8. FinBERT Sentiment
    # ─────────────────────────────────────────────────────────────────────
    sent_score, sent_label = stock_news_sentiment(symbol)

    if sent_score < -0.4 and "Mean Reversion" not in signal["strategies"]:
        return None

    # ─────────────────────────────────────────────────────────────────────
    # 9. Build Trade Setup
    # ─────────────────────────────────────────────────────────────────────
    return build_trade_setup(
        symbol=symbol,
        daily=daily,
        signal=signal,
        regime=regime,
        sent_score=sent_score,
        sent_label=sent_label,
    )


