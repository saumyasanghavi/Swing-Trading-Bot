def score_signals(daily: pd.DataFrame, weekly_bias: int,
                  regime: str) -> dict:
    """
    Evaluate all strategy signals on the daily chart.
    Returns a dict with score, strategies list, and direction.
    Score is normalised to 0–10.
    """
    w   = REGIME_WEIGHTS.get(regime, REGIME_WEIGHTS["RANGING"])
    d   = daily
    last = d.iloc[-1]
    prev = d.iloc[-2]

    strategies = []
    raw_score  = 0.0
    direction  = 0  # +1 long, -1 short

    # ── 1. TREND PULLBACK ────────────────────────────────────────────────
    # Price in uptrend (EMA20 > EMA50) but pulled back to EMA20 zone
    if (float(last["ema20"]) > float(last["ema50"]) and
            float(last["Close"]) < float(last["ema20"]) * 1.04 and
            float(last["Close"]) > float(last["ema20"]) * 0.97 and
            float(last["rsi"]) > 40 and float(last["adx"]) > 20):
        strategies.append("Trend Pullback")
        raw_score += w["Pullback"]
        direction = 1

    # ── 2. BREAKOUT WITH VOLUME ──────────────────────────────────────────
    high20 = d["High"].rolling(20).max().iloc[-2]  # -2 to avoid lookahead
    bb_squeeze = float(last["bb_width"]) < float(d["bb_width"].quantile(0.25))

    if float(last["Close"]) > float(high20):
        if float(last["vol_ratio"]) > 1.5:  # Volume surge
            strategies.append("Volume Breakout")
            raw_score += w["Breakout"]
            direction = 1
        elif bb_squeeze:  # Breakout from Bollinger squeeze
            strategies.append("Squeeze Breakout")
            raw_score += w["Breakout"] * 0.75
            direction = 1
        else:
            strategies.append("Breakout (Unconfirmed)")
            raw_score += w["Breakout"] * 0.4
            direction = 1

    # ── 3. MACD CROSSOVER (histogram turning positive) ───────────────────
    if (float(prev["macd_hist"]) < 0 and
            float(last["macd_hist"]) > 0 and
            float(last["macd"]) < 0):  # Crossover below zero line = early
        strategies.append("MACD Crossover")
        raw_score += 1.5
        direction = 1

    # ── 4. MEAN REVERSION (oversold bounce) ──────────────────────────────
    if (float(last["rsi"]) < 32 and
            float(last["stoch_k"]) < 25 and
            float(last["Close"]) > float(last["bb_lower"])):
        strategies.append("Mean Reversion")
        raw_score += w["MeanReversion"]
        direction = 1

    # ── 5. BULLISH RSI DIVERGENCE ─────────────────────────────────────────
    # Price makes lower low, RSI makes higher low
    window = 10
    if len(d) >= window + 2:
        price_lows_now  = float(d["Low"].iloc[-1])
        price_lows_prev = float(d["Low"].iloc[-window])
        rsi_now  = float(d["rsi"].iloc[-1])
        rsi_prev = float(d["rsi"].iloc[-window])

        if (price_lows_now < price_lows_prev and
                rsi_now > rsi_prev and
                rsi_now < 50):
            strategies.append("RSI Divergence")
            raw_score += w["Divergence"]
            direction = 1

    # ── 6. OBV CONFIRMATION ───────────────────────────────────────────────
    obv_trend = float(d["obv"].iloc[-1]) > float(d["obv"].ewm(span=20).mean().iloc[-1])
    if obv_trend and direction == 1:
        strategies.append("OBV Confirmed")
        raw_score += 0.5

    # ── 7. WEEKLY ALIGNMENT BONUS / PENALTY ──────────────────────────────
    if weekly_bias == 1 and direction == 1:
        raw_score += 1.0   # Weekly trend confirms daily signal
    elif weekly_bias == -1 and direction == 1:
        raw_score -= 1.5   # Trading against weekly trend → reduce score

    # ── 8. ADX STRENGTH BONUS ─────────────────────────────────────────────
    adx_val = float(last["adx"])
    if adx_val > 30:
        raw_score += 0.5
    elif adx_val < 15:
        raw_score -= 0.5

    # ── Normalise to 0–10 ─────────────────────────────────────────────────
    norm_score = min(10.0, max(0.0, (raw_score / 7.0) * 10.0))

    return {
        "score":      round(norm_score, 2),
        "strategies": strategies,
        "direction":  direction,
        "adx":        round(adx_val, 2),
    }


def build_trade_setup(symbol: str, daily: pd.DataFrame,
                      signal: dict, regime: str,
                      sentiment_score: float,
                      sentiment_label: str) -> dict:
    """
    Given a signal, build a complete trade setup with:
    - Dynamic ATR-based SL
    - Dual targets (T1 = 1.5R, T2 = 3R)
    - Trailing stop reference
    - Position size
    """
    last   = daily.iloc[-1]
    entry  = round(float(last["Close"]), 2)
    atr    = round(float(last["atr"]), 2)

    # Regime-adjusted ATR multipliers
    sl_mult = {
        "STRONG_BULL": 1.2, "MILD_BULL": 1.3,
        "RANGING":     1.5, "MILD_BEAR": 1.0, "STRONG_BEAR": 0.8,
    }.get(regime, 1.3)

    tgt1_mult = 1.5  # Conservative target (1.5 × risk)
    tgt2_mult = 3.0  # Extended target (3 × risk)

    sl       = round(entry - atr * sl_mult, 2)
    target1  = round(entry + atr * sl_mult * tgt1_mult, 2)
    target2  = round(entry + atr * sl_mult * tgt2_mult, 2)
    risk     = entry - sl
    rr       = round((target1 - entry) / risk, 2) if risk > 0 else 0

    # Sentiment adjustment on score
    final_score = round(
        signal["score"] + sentiment_score * CONFIG["SENTIMENT_WEIGHT"], 2
    )
    final_score = min(10.0, max(0.0, final_score))

    # Position size
    qty, capital_deployed = position_size(entry, sl)

    return {
        "symbol":           symbol,
        "entry":            entry,
        "sl":               sl,
        "target1":          target1,
        "target2":          target2,
        "atr":              atr,
        "rr":               rr,
        "score":            final_score,
        "strategies":       ", ".join(signal["strategies"]),
        "regime":           regime,
        "adx":              signal["adx"],
        "sentiment_score":  round(sentiment_score, 3),
        "sentiment_label":  sentiment_label,
        "qty":              qty,
        "capital_deployed": capital_deployed,
        "trailing_stop":    round(entry - atr * 2, 2),
        "date":             datetime.now().strftime("%Y-%m-%d"),
    }

# POSITION SIZING ENGINE
# ===========================================================================

def position_size(entry: float, sl: float) -> tuple:
    """
    Fixed Fractional Position Sizing.
    Risk exactly RISK_PER_TRADE_PCT of TOTAL_CAPITAL per trade.
    Returns (qty: int, capital_deployed: float)
    """
    risk_amount    = CONFIG["TOTAL_CAPITAL"] * CONFIG["RISK_PER_TRADE_PCT"]
    risk_per_share = entry - sl

    if risk_per_share <= 0:
        return 0, 0

    qty            = int(risk_amount / risk_per_share)
    capital        = round(qty * entry, 2)

    # Cap at MAX_OPEN_TRADES fraction of total capital
    max_capital    = CONFIG["TOTAL_CAPITAL"] / CONFIG["MAX_OPEN_TRADES"]
    if capital > max_capital:
        qty        = int(max_capital / entry)
        capital    = round(qty * entry, 2)

    return qty, capital

