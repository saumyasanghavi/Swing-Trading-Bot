def run_backtest(symbol: str, strategy: str = "breakout",
                 period: str = None) -> dict | None:
    """
    Backtest a single symbol using vectorbt.
    Supported strategies: 'breakout', 'pullback', 'mean_reversion'
    Returns a stats dictionary.
    """
    try:
        import vectorbt as vbt
    except ImportError:
        log.error("vectorbt not installed. Run: pip install vectorbt")
        return None

    period = period or CONFIG["BACKTEST_PERIOD"]

    try:
        ticker = yf.Ticker(symbol)
        df = ticker.history(period=period, interval="1d", auto_adjust=True, actions=False)
        if len(df) < 200:
          return None

        df = compute_indicators(df.copy())
        close = df["Close"]

        # ── Generate entry/exit signals by strategy ───────────────────────
        if strategy == "breakout":
          high20 = df["High"].rolling(20).max().shift(1)
          entries = (close > high20) & (df["vol_ratio"] > 1.5) & (df["adx"] > 20)
          exits   = (close < df["ema20"]) | (df["rsi"] > 75)

        elif strategy == "pullback":
            entries = (
                (df["ema20"] > df["ema50"]) &
                (close < df["ema20"] * 1.04) &
                (close > df["ema20"] * 0.97) &
                (df["rsi"] > 40) & (df["rsi"] < 65)
            )
            exits   = (close > df["ema20"] * 1.10) | (close < df["ema50"])

        elif strategy == "mean_reversion":
            entries = (df["rsi"] < 32) & (df["stoch_k"] < 25)
            exits   = (df["rsi"] > 60) | (df["stoch_k"] > 75)

        else:
            return None

        # ── Run vectorbt portfolio ─────────────────────────────────────────
        portfolio = vbt.Portfolio.from_signals(
            close,
            entries   = entries,
            exits     = exits,
            init_cash = CONFIG["TOTAL_CAPITAL"],
            fees      = CONFIG["BACKTEST_FEES"],
            sl_stop   = 0.05,  # 5% hard stop as safety net
            tp_stop   = 0.15,  # 15% hard take profit
        )

        stats = portfolio.stats()
        return {
            "symbol":        symbol,
            "strategy":      strategy,
            "total_return":  round(float(stats["Total Return [%]"]), 2),
            "sharpe":        round(float(stats.get("Sharpe Ratio", 0) or 0), 3),
            "max_dd":        round(float(stats["Max Drawdown [%]"]), 2),
            "win_rate":      round(float(stats["Win Rate [%]"]), 2),
            "total_trades":  int(stats["Total Trades"]),
            "avg_trade_pct": round(float(stats.get("Avg Winning Trade [%]", 0) or 0), 2),
            "portfolio":     portfolio,  # Full object for further analysis
        }

    except Exception as e:
        log.warning(f"[{symbol}] Backtest failed: {e}")
        return None


def backtest_universe(symbols: list = None, strategy: str = "breakout",
                      max_symbols: int = 50) -> pd.DataFrame:
    """
    Backtest a strategy across multiple symbols.
    Returns a DataFrame of results sorted by Sharpe ratio.
    """
    symbols = symbols or get_symbols()[:max_symbols]
    log.info(f"Backtesting {len(symbols)} symbols with '{strategy}' strategy...")

    results = []
    with concurrent.futures.ThreadPoolExecutor(max_workers=5) as ex:
        futures = {ex.submit(run_backtest, s, strategy): s for s in symbols}
        for i, future in enumerate(concurrent.futures.as_completed(futures)):
            if (i + 1) % 10 == 0:
                print(f"  Backtest progress: {i+1}/{len(symbols)}")
            try:
                r = future.result(timeout=120)
                if r:
                    results.append({k: v for k, v in r.items() if k != "portfolio"})
            except Exception as e:
                log.warning(f"Backtest thread error: {e}")

    if not results:
        print("No backtest results.")
        return pd.DataFrame()

    df = pd.DataFrame(results)
    df = df.sort_values("sharpe", ascending=False).reset_index(drop=True)

    print(f"\n{'='*70}")
    print(f"  BACKTEST RESULTS — Strategy: {strategy.upper()}")
    print(f"{'='*70}")
    print(df[["symbol","total_return","sharpe","max_dd",
              "win_rate","total_trades"]].to_string(index=False))

    return df

