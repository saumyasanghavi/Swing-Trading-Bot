@ttl_cache(seconds=86400)  # Cache for 24 hours
def fundamental_filter(symbol: str) -> tuple:
    """
    Quick fundamental health check using yfinance.info.
    Returns (passes: bool, reason: str)
    Filters out: high debt, negative ROE, low liquidity, high promoter pledge.
    """
    try:
        info = yf.Ticker(symbol).info

        # Market cap filter — skip micro caps (too illiquid for ₹25L)
        mkt_cap = info.get("marketCap", 0)
        if mkt_cap and mkt_cap < 1_000_000_000:  # < ₹100 Cr
            return False, "Market cap too small"

        # Debt / Equity
        de = info.get("debtToEquity")
        if de is not None and de > 200:
            return False, f"High D/E ratio: {de:.0f}"

        # Return on Equity
        roe = info.get("returnOnEquity")
        if roe is not None and roe < -0.05:
            return False, f"Negative ROE: {roe:.2%}"

        # Current Ratio (liquidity)
        cr = info.get("currentRatio")
        if cr is not None and cr < 0.8:
            return False, f"Low current ratio: {cr:.2f}"

        # Earnings growth (don't require positive but penalise big negatives)
        eg = info.get("earningsGrowth")
        if eg is not None and eg < -0.40:
            return False, f"Earnings declined sharply: {eg:.0%}"

        return True, "OK"

    except Exception as e:
        # If data unavailable, allow through (don't disqualify on missing data)
        return True, "Fundamental data unavailable — allowed through"

