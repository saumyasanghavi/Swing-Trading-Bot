# Add a threading lock around FinBERT inference in finbert_score()
import threading
_finbert_lock = threading.Lock()

def finbert_score(texts: list) -> float:
    if not texts:
        return 0.0
    model, tokenizer = load_finbert()
    scores = []
    with _finbert_lock:  # ← prevents concurrent GPU/CPU inference
        for i in range(0, len(texts), 8):
            batch = texts[i:i+8]
            inputs = tokenizer(batch, return_tensors="pt", truncation=True,
                               padding=True, max_length=128)
            with torch.no_grad():
                outputs = model(**inputs)
                probs = torch.softmax(outputs.logits, dim=-1).cpu().numpy()
            for p in probs:
                scores.append(float(p[0]) - float(p[1]))
    return float(np.mean(scores))

def load_finbert():
    """Lazy-load ProsusAI/finbert — finance-specific sentiment model."""
    global _finbert_model, _finbert_tokenizer
    if _finbert_model is None:
        log.info("Loading FinBERT model (first run only)...")
        model_name = "ProsusAI/finbert"
        _finbert_tokenizer = AutoTokenizer.from_pretrained(model_name)
        _finbert_model     = AutoModelForSequenceClassification.from_pretrained(model_name)
        _finbert_model.eval()
        if torch.cuda.is_available():
            _finbert_model = _finbert_model.cuda()
            log.info("FinBERT running on GPU ✓")
        else:
            log.info("FinBERT running on CPU")
    return _finbert_model, _finbert_tokenizer


def finbert_score(texts: list) -> float:
    """
    Score a list of financial headlines using FinBERT.
    Returns a float in [-1, +1]:
      +1 = strongly positive, -1 = strongly negative.
    FinBERT output labels: positive / negative / neutral
    """
    if not texts:
        return 0.0

    model, tokenizer = load_finbert()
    scores = []

    # Batch in groups of 8 to avoid OOM
    batch_size = 8
    for i in range(0, len(texts), batch_size):
        batch = texts[i:i+batch_size]
        inputs = tokenizer(
            batch, return_tensors="pt", truncation=True,
            padding=True, max_length=128
        )
        if torch.cuda.is_available():
            inputs = {k: v.cuda() for k, v in inputs.items()}

        with torch.no_grad():
            outputs = model(**inputs)
            probs   = torch.softmax(outputs.logits, dim=-1).cpu().numpy()

        # FinBERT label order: positive=0, negative=1, neutral=2
        for p in probs:
            score = float(p[0]) - float(p[1])  # positive - negative
            scores.append(score)

    return float(np.mean(scores))

# ── TTL Cache ─────────────────────────────────────────────────────────────

def ttl_cache(seconds: int = 600):
    def decorator(fn):
        cache = {}
        @functools.wraps(fn)
        def wrapper(*args):
            now = time.time()
            if args in cache and now - cache[args][1] < seconds:
                return cache[args][0]
            result = fn(*args)
            cache[args] = (result, now)
            return result
        return wrapper
    return decorator


