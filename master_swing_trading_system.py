@ttl_cache(seconds=1800)
def market_news_sentiment() -> float:
    """Aggregate FinBERT sentiment from multiple market news feeds."""
    titles = []
    for url in NEWS_FEEDS["market"]:
        try:
            feed = feedparser.parse(url)
            titles += [e.title for e in feed.entries[:5]]
        except:
            pass
    if not titles:
        return 0.0
    score = finbert_score(titles[:20])
    log.info(f"Market news sentiment: {score:+.3f}")
    return score


@ttl_cache(seconds=3600)
def stock_news_sentiment(symbol: str) -> tuple:
    """
    Get FinBERT sentiment for a specific stock.
    Returns (score: float, label: str)
    """
    query = symbol.replace(".NS", "").replace("-", " ")
    url   = NEWS_FEEDS["stock_template"].format(query=query)
    try:
        feed   = feedparser.parse(url)
        titles = [e.title for e in feed.entries[:CONFIG["SENTIMENT_SOURCES"]]]
        if not titles:
            return 0.0, "NEUTRAL"
        score = finbert_score(titles)
        label = "POSITIVE" if score > 0.1 else ("NEGATIVE" if score < -0.1 else "NEUTRAL")
        return round(score, 4), label
    except Exception as e:
        log.warning(f"Stock sentiment failed for {symbol}: {e}")
        return 0.0, "NEUTRAL"

