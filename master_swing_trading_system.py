def fetch_mtf_data(symbol: str) -> dict:
    tfs = {
        "daily":  ("6mo", "1d",  60),
        "weekly": ("2y",  "1wk", 52),
        "hourly": ("60d", "1h",  48),
    }
    result = {}
    ticker = yf.Ticker(symbol)  # ← use Ticker, not download

    for name, (period, interval, min_rows) in tfs.items():
        try:
            df = ticker.history(
                period=period,
                interval=interval,
                auto_adjust=True,    # split + dividend adjusted
                actions=False        # drop Dividends/Stock Splits columns
            )

            if df.empty:
                return {}

            # history() always returns flat columns — no MultiIndex issues
            df = df[["Open", "High", "Low", "Close", "Volume"]].copy()
            df = df.apply(pd.to_numeric, errors="coerce")
            df.dropna(subset=["Close", "High", "Low"], inplace=True)

            if len(df) < min_rows:
                return {}

            result[name] = df

        except Exception as e:
            log.warning(f"[{symbol}] {name} data fetch failed: {e}")
            return {}

    return result
    """
    Download Daily, Weekly, and Hourly OHLCV data for a symbol.
    Returns dict with keys: 'daily', 'weekly', 'hourly'
    """
    tfs = {
        "daily":  ("6mo", "1d",  60),
        "weekly": ("2y",  "1wk", 52),
        "hourly": ("60d", "1h",  48),
    }
    result = {}
    for name, (period, interval, min_rows) in tfs.items():
        try:
            df = yf.download(
                symbol, period=period, interval=interval,
                progress=False, auto_adjust=True
            )

            # Flatten MultiIndex columns
            # ✅ FIX: Always drop the ticker level correctly
            if isinstance(df.columns, pd.MultiIndex):
              df.columns = df.columns.get_level_values(0)  # Gets the Price field (Close, Open, etc.)

            # Drop duplicate columns
            df = df.loc[:, ~df.columns.duplicated()]

            # Keep only OHLCV
            df = df[["Open", "High", "Low", "Close", "Volume"]].copy()
            df = df.apply(pd.to_numeric, errors="coerce")
            df.dropna(subset=["Close", "High", "Low"], inplace=True)

            if len(df) < min_rows:
                return {}

            result[name] = df

        except Exception as e:
            log.warning(f"[{symbol}] {name} data fetch failed: {e}")
            return {}
    return result


def compute_indicators(df: pd.DataFrame) -> pd.DataFrame:
    """Add a full indicator suite to an OHLCV DataFrame."""
    c = df["Close"].squeeze().astype(float)
    h = df["High"].squeeze().astype(float)
    l = df["Low"].squeeze().astype(float)
    v = df["Volume"].squeeze().astype(float)

    # Trend
    df["ema20"]       = ta.trend.ema_indicator(c, 20)
    df["ema50"]       = ta.trend.ema_indicator(c, 50)
    df["ema200"]      = ta.trend.ema_indicator(c, 200) if len(df) >= 200 else np.nan
    df["supertrend"]  = ta.trend.stc(c)

    # ADX
    adx_ind           = ta.trend.ADXIndicator(h, l, c, 14)
    df["adx"]         = adx_ind.adx()
    df["adx_pos"]     = adx_ind.adx_pos()
    df["adx_neg"]     = adx_ind.adx_neg()

    # MACD
    macd_ind          = ta.trend.MACD(c)
    df["macd"]        = macd_ind.macd()
    df["macd_signal"] = macd_ind.macd_signal()
    df["macd_hist"]   = macd_ind.macd_diff()

    # Momentum
    df["rsi"]         = ta.momentum.rsi(c, 14)
    df["stoch_k"]     = ta.momentum.stoch(h, l, c, 14, 3)
    df["stoch_d"]     = ta.momentum.stoch_signal(h, l, c, 14, 3)
    df["roc"]         = ta.momentum.roc(c, 10)

    # Volatility
    df["atr"]         = ta.volatility.average_true_range(h, l, c, 14)
    bb                = ta.volatility.BollingerBands(c, 20, 2)
    df["bb_upper"]    = bb.bollinger_hband()
    df["bb_lower"]    = bb.bollinger_lband()
    df["bb_width"]    = bb.bollinger_wband()
    df["bb_pct"]      = bb.bollinger_pband()

    # Volume
    df["obv"]         = ta.volume.on_balance_volume(c, v)
    df["vol_ma20"]    = v.rolling(20).mean()
    df["vol_ratio"]   = v / df["vol_ma20"]

    return df


def weekly_trend_bias(weekly_df: pd.DataFrame) -> int:
    """Returns +1 (bullish), -1 (bearish), 0 (neutral) based on weekly chart."""
    w = weekly_df
    if len(w) < 52:
        return 0
    c      = w["Close"].squeeze().astype(float)
    ema20w = float(c.ewm(span=20).mean().iloc[-1])
    ema50w = float(c.ewm(span=50).mean().iloc[-1])
    price  = float(c.iloc[-1])
    rsi_w  = float(ta.momentum.rsi(c, 14).iloc[-1])

    bull = sum([price > ema20w, ema20w > ema50w, rsi_w > 50])
    bear = sum([price < ema20w, ema20w < ema50w, rsi_w < 50])

    if bull >= 2: return 1
    if bear >= 2: return -1
    return 0


def hourly_entry_confirmation(hourly_df: pd.DataFrame, direction: int) -> bool:
    """Confirm entry on hourly chart for better timing."""
    if hourly_df is None or len(hourly_df) < 24:
        return True
    h          = hourly_df.copy()
    c          = h["Close"].squeeze().astype(float)
    h["ema9"]  = c.ewm(span=9).mean()
    h["ema21"] = c.ewm(span=21).mean()
    last       = h.iloc[-1]
    prev       = h.iloc[-2]

    if direction == 1:
        return (
            float(last["ema9"])  > float(last["ema21"]) and
            float(last["Close"]) > float(prev["Close"])
        )
    return True

